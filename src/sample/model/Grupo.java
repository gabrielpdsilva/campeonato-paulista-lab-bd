package sample.model;

public class Grupo {

    public Grupo() {
        super();
    }

    private String grupo;
    private Time time;

    public String getGrupo() {
        return grupo;
    }

    public void setGrupo(String grupo) {
        this.grupo = grupo;
    }

    public Time getTime() {
        return time;
    }

    public void setTime(Time time) {
        this.time = time;
    }
}
